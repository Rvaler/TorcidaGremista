import UIKit

extension UIColor {
    
    static var gremioBlueColor : UIColor {
        return UIColor(red: 25/255, green: 146/255, blue: 219/255, alpha: 1.0)
    }
    static var gremioBlackColor : UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 1.0)
    }
    static var gremioWhiteColor : UIColor {
        return UIColor(red: 255, green: 255, blue: 255, alpha: 1.0)
    }
}
